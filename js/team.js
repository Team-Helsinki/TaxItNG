    let divElement = document.createElement("div")
    let idElement = document.createElement("p")
    let nameElement = document.createElement("h4")
    let roleElement = document.createElement("p")
    let contributionElement = document.createElement("h5")
    let cvlinkElement = document.createElement("p")
        //let idElement = document.createElement("p")
    let idContent = document.createTextNode(member.id)
    let nameContent = document.createTextNode(member.name)
    let roleContent = document.createTextNode(member.role)
    contributionElement.innerHTML = `${member.contribution} `
    cvlinkElement.innerHTML = `<a href=${member.cvlink}><img src="https://res.cloudinary.com/oluwamayowaf/image/upload/v1567633673/Asset_2_1.5x_t1bda4.png"></a>`
        //let idContent = document.createTextNode(member.id)
    divElement.className = "member"
    idElement.appendChild(idContent)
